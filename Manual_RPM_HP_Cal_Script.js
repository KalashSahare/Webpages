  function CreateDropdownfromData(data,dropdown) {
    data.slice(1).forEach(rowData => {
      const option = document.createElement('option');
      option.value = rowData.join('|');  // Set option value to the name
      option.textContent = rowData[0];  // Display the name
     dropdown.appendChild(option);
    });
  }
  
    function GET_DESCP_FROM_MISC_CODE(code,data) {
      arr=[];
      for (let i=1; i<=5; i++){
        if (i<=code.length){
          let desc = data.find(row => row[1]===code[i-1])?.[0];
          let value = code[i-1];
          arr.push([desc,value]);
          
        } else {
          arr.push(["No Description",""]);
        }
      }
      return arr
    }
  
    function GenerateTable(data,table) {
      // Loop through the array to create table rows and cells
      data.forEach((rowData, rowIndex) => {
        const row = document.createElement('tr');
        
        rowData.forEach((cellData, colIndex) => {
          const cell = rowIndex === 0 ? document.createElement('th') : document.createElement('td');
          cell.textContent = cellData;
          row.appendChild(cell);
        });
  
        table.appendChild(row);
      });
    }
  
